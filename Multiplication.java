class Multiplication
{
    public static void main(String[] args)
    {
        int k=2;
        int i;
        while(k<=12)
        {
            for( i=1;i<=10;i++)
            {
                System.out.println(k +"*"+ i +"=" + (k*i));


            }
            k++;
        }
        

    }
}
